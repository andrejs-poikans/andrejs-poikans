function openForm() {
    document.getElementById("myForm").style.display = "block";
    document.getElementById("form-canvas").style.display = "block";
    document.getElementById("close-form").style.display = "block";
  }
  
function closeForm() {
    document.getElementById("myForm").style.display = "none";
    document.getElementById("form-canvas").style.display = "none";
    document.getElementById("close-form").style.display = "none";
}